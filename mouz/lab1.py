def sleep_in(weekday, vacation):
	return weekday == False or vacation == True

def negate(n):
	return n * -1

def between(number,a,b):
	return number > a and number < b

def square(x):
	return x ** 2

def funkyCalc(x,y,z):
	return ((x * 3) + (y * 0.5)) / (z * 2)

def ftoc(f):
	return (f - 32) / 1.8

def isBig(n):
	return n > 10000

def isEven(n):
	return n % 2 == 0

def monkey_trouble(a_smile, b_smile):
	return a_smile == b_smile
